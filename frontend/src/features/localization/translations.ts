function interpolate(template: string, params?: Record<string, string | number>) {
  if (!params) {
    return template
  }

  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? ''))
}

function resolveValue(tree: TranslationTree, key: string): string {
  const segments = key.split('.')
  let current: TranslationTree | TranslationValue | undefined = tree

  for (const segment of segments) {
    if (typeof current === 'string' || !current || !(segment in current)) {
      return key
    }

    current = current[segment]
  }

  return typeof current === 'string' ? current : key
}

export function translate(
  language: LanguagePreference,
  key: string,
  params?: Record<string, string | number>,
) {
  return interpolate(resolveValue(translations[language], key), params)
}
